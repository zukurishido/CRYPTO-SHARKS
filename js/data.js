// –°–æ—Å—Ç–æ—è–Ω–∏–µ —Å–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏–∏
const syncState = {
    lastSync: null,
    isSyncing: false,
    lastError: null,
    version: null,
    retryCount: 0
};

// –ó–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö —Å JSONBin
async function loadData() {
    try {
        if (syncState.isSyncing) return;
        syncState.isSyncing = true;
        showSyncStatus('–°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏—è...');

        // –ü–æ–ø—ã—Ç–∫–∞ –∑–∞–≥—Ä—É–∑–∏—Ç—å —Å JSONBin
        const response = await fetch(`https://api.jsonbin.io/v3/b/${SYNC_CONFIG.binId}/latest`, {
            headers: {
                'X-Master-Key': SYNC_CONFIG.key,
                'X-Bin-Meta': 'false'
            }
        });

        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        const remoteData = await response.json();

        // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Å—Ç—Ä—É–∫—Ç—É—Ä—ã –¥–∞–Ω–Ω—ã—Ö
        YEARS.forEach(year => {
            if (!remoteData[year]) remoteData[year] = {};
            MONTHS.forEach(month => {
                if (!remoteData[year][month]) {
                    remoteData[year][month] = {};
                    CATEGORIES.forEach(category => {
                        if (!remoteData[year][month][category]) {
                            remoteData[year][month][category] = { trades: [] };
                        }
                    });
                }
            });
        });

        data = remoteData;
        localStorage.setItem('cryptoSharksData', JSON.stringify(data));
        syncState.lastSync = Date.now();
        syncState.lastError = null;
        showSyncStatus('–î–∞–Ω–Ω—ã–µ –æ–±–Ω–æ–≤–ª–µ–Ω—ã', 'success');
        updateContent();
        return true;
    } catch (error) {
        console.error('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –¥–∞–Ω–Ω—ã—Ö:', error);
        syncState.lastError = error.message;
        showSyncStatus(`–û—à–∏–±–∫–∞ —Å–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏–∏: ${error.message}`, 'error');
        
        // –ó–∞–≥—Ä—É–∑–∫–∞ –ª–æ–∫–∞–ª—å–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö –ø—Ä–∏ –æ—à–∏–±–∫–µ
        const savedData = localStorage.getItem('cryptoSharksData');
        if (savedData) {
            data = JSON.parse(savedData);
        }
        return false;
    } finally {
        syncState.isSyncing = false;
    }
}

// –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –≤ JSONBin
async function saveData() {
    try {
        if (syncState.isSyncing) return false;
        syncState.isSyncing = true;
        showSyncStatus('–°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ...');

        const response = await fetch(`https://api.jsonbin.io/v3/b/${SYNC_CONFIG.binId}`, {
            method: 'PUT',
            headers: {
                'Content-Type': 'application/json',
                'X-Master-Key': SYNC_CONFIG.key,
                'X-Bin-Versioning': 'false'
            },
            body: JSON.stringify(data)
        });

        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        localStorage.setItem('cryptoSharksData', JSON.stringify(data));
        syncState.lastSync = Date.now();
        showSyncStatus('–°–æ—Ö—Ä–∞–Ω–µ–Ω–æ', 'success');
        return true;
    } catch (error) {
        console.error('–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è:', error);
        syncState.lastError = error.message;
        showSyncStatus(`–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è: ${error.message}`, 'error');
        return false;
    } finally {
        syncState.isSyncing = false;
    }
}

// –ü–æ–∫–∞–∑ —Å—Ç–∞—Ç—É—Å–∞ —Å–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏–∏
function showSyncStatus(message, type = 'info') {
    const statusContainer = document.getElementById('syncStatus') || createSyncStatusElement();
    statusContainer.className = `sync-status ${type}`;
    statusContainer.innerHTML = `
        <span class="sync-message">${message}</span>
        ${type === 'error' ? '<button onclick="retrySync()" class="retry-btn">–ü–æ–≤—Ç–æ—Ä–∏—Ç—å</button>' : ''}
    `;

    if (type !== 'error') {
        setTimeout(() => {
            statusContainer.classList.add('fade-out');
        }, 3000);
    }
}

